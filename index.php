<script>
	function Rotator(start_from){
		var phrases = ["University Complutense, Madrid, Spain","University Alfonso X, Madrid, Spain","University of Valencia, Spain","University of Padua, Italy","University of Genova, Italy","University of Carthagena, Colombia","University of Arraçatuba, Brasil","University of New York, USA"];
		var total = phrases.length;
		var interval = 1000;
		if(void 0 === start_from){
			start_from = 0;
		}
		$(".textrotator").text(phrases[start_from]).animate({"opacity":"1"}, 850, function(){
			if(start_from >= (total-1)){
				setTimeout(function(){
					$(".textrotator").animate({"opacity":"0"}, 850, function(){
						Rotator();
					});
				}, interval);
			}else{
				start_from++;
				setTimeout(function(){
					$(".textrotator").animate({"opacity":"0"}, 850,function(){
						Rotator(start_from);
					});
				}, interval);
			}
		})
	}
	$(document).ready(function(){
		Rotator();
	})
</script>

